function forceLines() {
  if (forcesShown == true) {
    forcesShown = false
  } else {
    forcesShown = true
  }
}

function screenSaverMode() {
  if (screenSaver == true) {
    screenSaver = false
  } else {
    screenSaver = true
  }
}

function startPause() {
  if (running == true) {
    running = false
  } else {
    running = true
  }
}